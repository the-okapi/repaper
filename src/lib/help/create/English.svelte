<h1 class="h1">How to Create a Document</h1>

<p>First, head to <strong>Create a Document</strong> in the sidebar.</p>
<br />

<p>
	Choose a title for your document, and type it in the <strong>Document Title</strong> box.<br />You
	can change this anytime.
</p>
<br />

<p>
	The <strong>Document Code</strong> is like a unique name for your document.<br />
	It is used when you are sharing the document with other people.<br />
	It can only contain lowercase letters, numbers and hyphens.
</p>

